function _mapapi_getResourceRoot() {
	if (document.URL.match(/http:/)){
		var url = document.URL.split("?")[0];
		var urlA = url.split("/");
		for ( var i=0; i<urlA.length-1; i++){
			__resourceRoot += urlA[i]+"/";
		}
	}else{
		__resourceRoot = "";
	}
}
