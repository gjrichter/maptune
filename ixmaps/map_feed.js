/**
 * add a feed to the map  
 * @param the URL of the feed
 * @param opt optional parameter object
 * @type void
 */
	ixmaps.feed.addFeed = function(szSource,opt,callback){

		// if called by setTimeout(), opt is a string (see below)
		// ------------------------------------------------------
		if ( typeof(opt) == "string" ){
			opt = JSON.parse(opt);
			// no zoomto after refresh
			opt.flag = "";
		}

		_TRACE("ixmaps.feed.addFeed: "+szSource);

		if ( typeof(opt) == "undefined" || !opt || !opt.type || !opt.format ){
			var szParam =	(!opt.type?"type ":"") +  
							(!opt.format?"format ":"");
			ixmaps.errorMessage("addFeed: missing parameter: "+szParam,2000);
			return false;
		}

		ixmaps.feed.unresolvedPosition = 0;

		if ( !callback ){
			callback = function(){};
		}

		// GR 23.05.2013 pass url for isLayerOfSource()
		opt.szSourceUrl = szSource;
		// refresh option ?
		// ----------------------------------------------------------------------
		if ( opt && opt.opt && opt.opt.refresh ){
			opt.refreshTimeout = setTimeout("ixmaps.feed.addFeed('"+szSource+"','"+JSON.stringify(opt)+"')",opt.opt.refresh*1000);
		}

		// check origin and get around cross domain restrictions by using a proxy
		// ----------------------------------------------------------------------
		if ( typeof(opt.proxy) == "undefined" || !opt.proxy ){
			if ( ixmaps.feed.crossDomain(szSource,opt,callback) ){
				return;
			}
		}

		if ( opt.format == 'json' ){
			ixmaps.message("loading",true);
			$.get(szSource,
				function(data){
					callback(ixmaps.feed.processFeed(data,opt));
				}).error(function() { alert("error"); });
		}
		if ( opt.format == 'jsonp' ){
			ixmaps.message("loading",true);
			$.getScript(szSource,
				function(data){
					eval("var dataP = "+opt.opt.JsonpCallBack);
					callback(ixmaps.feed.processFeed(dataP,opt));
				}).error(function() { alert("error"); });
		}
		if ( opt.format == 'text' ){
			ixmaps.message("loading",true);
            $.ajax({
                 type: "GET",
                 url: szSource,
                 dataType: "text",
                 success: function(xml) {
					// alert(xml);
					var test = JSON.parse(xml);
					// alert(test.value.items[0].content);
					szTest = "{\"map\":"+String(test.value.items[0].content);
					// alert(szTest);
					eval ("var obj='"+szTest+"');");
					// alert(obj);
					callback(ixmaps.feed.processFeed(xml,opt));
                 },
                 error: function(xml) {
					callback(false);
                 }
            });
		}
		if ( opt.format == 'xml' ){
			ixmaps.message("loading",true);
            $.ajax({
                 type: "GET",
                 url: szSource,
                 dataType: "xml",
                 success: function(xml) {
					callback(ixmaps.feed.processFeed(xml,opt));
                 },
                 error: function(xml) {
 					callback(false);
                }
            });
		}
		if ( opt.format == 'csv' ){
			ixmaps.message("loading",true);
			$.ajax({
				type: "GET",
				url: szSource,
				dataType: "text",
				success: function(data) {
					callback(ixmaps.feed.processFeed(data,opt));
				},
				error: function() {
 					callback(false);
				}
			});
		}
	};


/**
 * process the feeds data feed to the map  
 * @param the data object received from the feed
 * @param opt optional parameter object
 * @type void
 */
	ixmaps.feed.processFeed = function(data,opt){

		_TRACE("ixmaps.feed.processFeed: " + opt.type);

		if ( opt.format == "json" && typeof(data) == "string" ){
			data = JSON.parse(data);
		}

		if ( data == null ){
			ixmaps.message("loading",false,"x7");
			return false;
		}

		// parse the data and create ixMaps layer object
		// ---------------------------------------------
//		try {
			switch(opt.type){
				// deprecated, keep for compatibility, use 'MyMapsKLM_YQL'
				case 'Google_Maps_My_Map_KML_YQL':
					var feedLayer = this.parse.MyMapsKLM_YQL(data,opt);
					break;
				// deprecated, keep for compatibility, use 'MyMapsKLM'
				case 'Google_Maps_My_Map_KML':
					var feedLayer = this.parse.MyMapsKLM(data,opt);
					break;
				// the one and only for the future
				default:
					if ( eval("typeof(this.parse."+opt.type+")") == "function" ){
						var feedLayer = eval("this.parse."+opt.type+"(data,opt)");
					}else{
						_TRACE("load parser: " + 'map_feed.parse.' + opt.type + ".js");
						_TRACE("load parser: " + scriptBase + 'map_feed.parse.' + opt.type + ".js");

						$.getScript(scriptBase + 'map_feed.parse.' + opt.type + ".js")
						.done(function(script, textStatus) {
						  ixmaps.feed.processFeed(data,opt);
						  return;
						})
						.fail(function(jqxhr, settings, exception) {
							_TRACE("load parser: " + scriptBase + 'map_feed.parse.' + opt.type + ".js");
							$.getScript(scriptBase + 'plugins/map_feed.parse.' + opt.type + ".js")
							.done(function(script, textStatus) {
							  ixmaps.feed.processFeed(data,opt);
							  return;
							})
							.fail(function(jqxhr, settings, exception) {
							  ixmaps.message("loading",false,"x8");
							  ixmaps.errorMessage("'"+opt.type+"' unknown feed format !",2000);
							});
						});

						return;
					}
					break;
			}
//		} catch (e){
//			ixmaps.errorMessage("'"+opt.type+"' unknown feed format !",2000);
//			return false;
//		}

		if ( feedLayer == null ){
			alert("no valid data");
			return false;
		}
		if ( feedLayer == "loading parser" ){
			return false;
		}

		if ( ixmaps.feed.unresolvedPosition ){
			ixmaps.errorMessage(ixmaps.feed.unresolvedPosition+" unresolved position(s)",2000);
		}

		// some display options for the new layer
		// ---------------------------------------------
		if ( opt.flag && opt.flag.match(/gallery/) ){
			feedLayer.layers[0].properties.fGallery = true;
		}
		if ( opt.flag && opt.flag.match(/open/) ){
			feedLayer.layers[0].sourcelegend = "expanded";
			feedLayer.layers[0].properties.open = true;
			feedLayer.layers[0].properties.visibility = true;
		}
		if ( opt.flag && opt.flag.match(/closed/) ){
			feedLayer.layers[0].sourcelegend = "collapsed";
//			feedLayer.layers[0].properties.open = false;
		}
		if ( opt.flag && opt.flag.match(/noinfo/) ){
			feedLayer.layers[0].fShowInfoInList = false;
		}
		if ( opt.flag && opt.flag.match(/showinfo/) ){
			feedLayer.layers[0].fShowInfoInList = true;
		}
		if ( opt.flag && opt.flag.match(/sort/) ){
			feedLayer.layers[0].fSort = true;
		}

		if ( opt.opt ){
			if ( opt.opt.layer ){
				if ( opt.opt.layer.length ){
					for ( l in  opt.opt.layer ){
						ixmaps.jsapi.setLayerParam(opt.opt.layer[l]);
					}
				}else{
					ixmaps.jsapi.setLayerParam(opt.opt.layer);
				}
			}
			if ( opt.opt.refresh && opt.refreshTimeout ){
				feedLayer.layers[0].refreshTimeout = opt.refreshTimeout;
			}
		}

		// GR 23.05.2013 pass url
		feedLayer.szSourceUrl = opt.szSourceUrl;
		// finally add the layer 
		// ---------------------------------------------
		_TRACE("addNewData from feed: "+feedLayer.name);
		ixmaps.jsapi.addNewData(feedLayer.name?feedLayer.name:"feed-test",feedLayer);
		ixmaps.message("loading",false,"x9");

		if ( opt.flag && opt.flag.match(/zoomto/) ){
			ixmaps.jsapi.zoomToLayer(feedLayer.name);
		}

		return true;

	};



