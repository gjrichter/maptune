// Auto-load scripts
//
// specify which feed parser to load by using
// <script src="map_feed.js?(feedtype1,feedtype2)" ...
// in your HTML
//
// for each feed type map_feed.feedtype.js will be loaded
// modules 'map_feed.parse.geojson.js,map_feed.parse.geojsonr.js,map_feed.parse.georss.js,map_feed.parse.kml.js' are always loaded
//
var scriptBase;
(function() {
	var providers = null;
	var modules = 'georss,geojson,geojsonr,kml';
	var scripts = document.getElementsByTagName('script');
	// Determine which scripts we need to load	
	for (var i = 0; i < scripts.length; i++) {
		var match = scripts[i].src.replace(/%20/g , '').match(/^(.*?)map_feed\.js(\?\(\[?(.*?)\]?\))?$/);
		if (match !== null) {
			scriptBase = match[1];
			if (match[3]) {
				var settings = match[3].split(',[');
				providers = settings[0].replace(']' , '');
				modules += ',' + providers;
				if (settings[1]) {
					modules += ',' + settings[1];
				}
			}
			break;
	   }
	}
	modules = modules.replace(/ /g, '').split(',');

	// load the parse scripts
	var scriptTagStart = '<script type="text/javascript" src="' + scriptBase + 'map_feed.parse.';
	var scriptTagEnd = '.js"></script>';
	var scriptsAry = [];
	for (i = 0; i < modules.length; i++) {
		scriptsAry.push(scriptTagStart + modules[i] + scriptTagEnd);
	}
	// load cross domain script
	scriptsAry.push('<script type="text/javascript" src="' + scriptBase + 'map_feed.cross.js"></script>');
	// load fusion tables script
	scriptsAry.push('<script type="text/javascript" src="' + scriptBase + 'map_feed.FusionTables.js"></script>');
	scriptsAry.push('<script type="text/javascript" src="' + scriptBase + 'map_feed.query.FusionTables.js"></script>');
	scriptsAry.push('<script type="text/javascript" src="http://www.google.com/jsapi"></script>');

	document.write(scriptsAry.join(''));
})();

